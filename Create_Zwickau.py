def haversine(a,b):
    b1,l1=a
    b2,l2=b
    b1=np.deg2rad(b1)
    b2=np.deg2rad(b2)
    l1=np.deg2rad(l1)
    l2=np.deg2rad(l2)

    del_l=l1-l2
    del_b=b1-b2
    a=np.square(np.sin(del_b/2))+np.cos(b1)*np.cos(b2)*np.square(np.sin(del_l/2))
    c=6371000*2*np.arcsin(np.sqrt(a))
    return c 
